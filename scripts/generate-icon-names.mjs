// Generate a TypeScript union of icon names from the SVGs in src/lib/icons/,
// so <Icon name="…"> gets editor autocomplete + typo checking (see Icon.svelte).
// Runs automatically via the predev/prebuild npm hooks; run manually with:
//   node scripts/generate-icon-names.mjs
import { globSync } from 'node:fs';
import { writeFile } from 'node:fs/promises';

const OUT = 'src/lib/components/icon-names.d.ts';

const names = globSync('src/lib/icons/*.svg')
  .map((path) => path.replace(/\\/g, '/').split('/').pop().replace(/\.svg$/, ''))
  .sort();

if (names.length === 0) {
  console.error('No SVGs found under src/lib/icons/ — refusing to write an empty union.');
  process.exit(1);
}

const union = names.map((name) => `  | '${name}'`).join('\n');
const contents = `// AUTO-GENERATED by scripts/generate-icon-names.mjs — do not edit by hand.
// Run \`node scripts/generate-icon-names.mjs\` (or any dev/build) to regenerate.
export type IconName =
${union};
`;

await writeFile(OUT, contents);
console.log(`Wrote ${names.length} icon names to ${OUT}`);
